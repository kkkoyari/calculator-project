function add (a, b) {
	let result = a + b;
	return result;
}

function subtract (a, b) {
	let result = a - b;
	return result;
}

function multiply (a, b) {
	let result = a * b;
	return result;
}

function divide (a, b) {
	let result = a / b;
	return result;
}

console.log(add(0.57, 89));
console.log(subtract(0.99, 0.4));
console.log(multiply(10, 2));
console.log(divide(0.9, 0.3));